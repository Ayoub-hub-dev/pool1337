void	ft_rev_int_tab(int *tab, int size)
{
	int		x;
	int		i;

	i = 0;
	while (i < size - 1)
	{
		x = tab[i];
		tab[i] = tab[size - 1];
		tab[size - 1] = x;
		size--;
		i++;
	}
}
