#include <unistd.h>

void	ft_is_negative(int n)
{
	char	ne;
	char	po;

	ne = 'N';
	po = 'P';
	if (n < 0)
	{
		write(1, &ne, 1);
	}
	else
	{
		write(1, &po, 1);
	}
}
